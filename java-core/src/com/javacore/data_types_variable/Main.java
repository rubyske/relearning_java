package com.javacore.data_types_variable;

public class Main {
    public static void main(String[] args) {
        System.out.println("Ini merupakan Contoh tipe data dan variable");
        System.out.println("Tipe data Primitive");
        int usia = 21;
        float berat = 60.5f;
        boolean aktif = true;
        char inisial = 'B';

        System.out.println("Usia: " + usia);
        System.out.println("Berat: " + berat);
        System.out.println("Inisial: " + inisial);
        System.out.println("Aktif: " + aktif);

        System.out.println("Tipe data reference");
        String nama = "Muhamad Badru Kamal";
        System.out.println("Nama: " + nama);
    }
}
