package com.javacore.operator_java;

public class Task01 {
    public static void main(String[] args) {
        int a = 10;
        int b = 7;

        System.out.println("penjumlahan");
        int resultA = a + b;
        System.out.println("10 + 7 = " + resultA);

        System.out.println("Pengurangan");
        int resultB = a - b;
        System.out.println("10 - 7 = " + resultB);

        System.out.println("Modulus");
        int resultC = a % b;
        System.out.println("10 % 7 = " + resultC);
    }
}
